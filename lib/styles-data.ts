export function getStyleBySlug(slug: string): StyleData | undefined {
  return STYLES_DATABASE.find(style => style.slug === slug);
}

export function getImplementedStyles(): StyleData[] {
  return STYLES_DATABASE.filter(style => style.implemented);
}

export function getStylesByType(type: StyleData['type']): StyleData[] {
  return STYLES_DATABASE.filter(style => style.type === type);
}

export function getSkillStyles(): StyleData[] {
  return STYLES_DATABASE.filter(style => style.usedSkill);
}

export function getNonSkillStyles(): StyleData[] {
  return STYLES_DATABASE.filter(style => !style.usedSkill);
}
